<?php
// Определяем адрес почты, на который будем отправлять запросы
$email_address = "Kseniaamf@icloud.com";

// Проверяем, что форма была отправлена
if (isset($_POST["submit"])) {
  // Получаем данные из формы
  $name = $_POST["name"];
  $phone = $_POST["phone"];

  // Создаем тему сообщения
  $subject = "Заявка на постановку автомобиля на учет";

  // Создаем содержимое сообщения
  $message = "Имя: $name\nТелефон: $phone";

  // Отправляем сообщение на почту
  mail($email_address, $subject, $message);

  // Выводим сообщение об успешной отправке
  echo "Ваша заявка была отправлена. Спасибо!";
}
?>
